#pragma once

namespace Core{

		//-----------------------------------------------------------------------------
		//!
		class IGameState;

		//-----------------------------------------------------------------------------
		//!
		class IGameStateManager
		{
		public:
				//-----------------------------------------------------------------------------
				//!
				static IGameStateManager* Create();

				//-----------------------------------------------------------------------------
				//!
				virtual ~IGameStateManager(){}

				//-----------------------------------------------------------------------------
				//!
				virtual void AddGameState(const char* strName, IGameState* pState) = 0;

				//-----------------------------------------------------------------------------
				//!
				virtual IGameState* FindByName(const char* strName) = 0;

				//-----------------------------------------------------------------------------
				//!
				virtual void Change(IGameState* pState) = 0;

				//-----------------------------------------------------------------------------
				//!
				virtual bool Push(IGameState* pState) = 0;

				//-----------------------------------------------------------------------------
				//!
				virtual void Pop() = 0;

				//-----------------------------------------------------------------------------
				//!
				virtual void Update(float timeDelta) = 0;
		};

} //Core