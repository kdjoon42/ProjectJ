#include <Main/IEngine.h>

#include <OgreRoot.h>

namespace Main{

		//-----------------------------------------------------------------------------
		//!
		class Engine: public IEngine
		{
		public:
				//-----------------------------------------------------------------------------
				//!
				Engine(const GameConfig& gc);

				//-----------------------------------------------------------------------------
				//!
				virtual ~Engine();
				
		protected:

				Ogre::Root* m_pRoot;

		};

		//////////////////////////////////////////////////////////////////////////
		//

		//-----------------------------------------------------------------------------
		IEngine* IEngine::Create(const GameConfig& gc)
		{
				return new Engine(gc);
		}

		//-----------------------------------------------------------------------------
		Engine::Engine(const GameConfig& gc)
		{
				m_pRoot = new Ogre::Root();
		}

		//-----------------------------------------------------------------------------
		Engine::~Engine()
		{
				delete m_pRoot;
		}
		


}//Main